"""helper file"""


def add(first_number, second_number):
    """
    :param first_number:
    :param second_number:
    :return: sum of the two numbers
    """

    return first_number + second_number


def sub(first_number, second_number):
    """
    :param first_number:
    :param second_number:
    :return: difference of the between the first and second number
    """

    return first_number - second_number
